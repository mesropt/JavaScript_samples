/*
1. Создать функцию, генерирующую шахматную доску. При этом можно
использовать любые html-теги по своему желанию. Доска должна быть
разлинована соответствующим образом, т.е. чередовать черные и белые
ячейки. Строки должны нумероваться числами от 1 до 8, столбцы –
латинскими буквами A, B, C, D, E, F, G, H. (использовать createElement /
appendChild)
 */

document.body.insertAdjacentHTML('beforebegin', '<div><i>Hello!</i></div>');
document.body.insertAdjacentHTML('afterbegin', '<div><i>Hello!</i></div>');
document.body.insertAdjacentHTML('beforeend', '<div><i>Hello!</i></div>');
document.body.insertAdjacentHTML('afterend', '<div><i>Hello!</i></div>');